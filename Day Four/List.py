#list indexing
my_list = ["neha","roshni","shruti"]
print(my_list)
print(my_list[0])
print(my_list[-1])

# List slicing
my_list = ['m', 'a', 'n', 'g', 'o']
print (my_list)          
print(my_list[0:3])     
print(my_list[-2:])     
print(my_list[:-1])     
print(my_list[::-1])    
print(my_list[1:4])

# list add,remove and sort

# add list
my_var = ["apple", "banana", "cherry"]
my_var.append("orange")
print(my_var)  

#sort list
my_var = ["apple", "banana", "cherry"]
my_list.sort()
print(my_list)

# Remove list
my_var = ["apple", "banana", "cherry"]
my_var.remove("banana")
